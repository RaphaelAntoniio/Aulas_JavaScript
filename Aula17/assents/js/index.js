/* icones de comparação
* > maior que*/
let maior = 10;
let maior1 = 11;
console.log(maior>maior1);

//* >=maior que igual a
console.log(maior>=maior1);
// < menor que
console.log(maior<maior1);

// <=menor que igual a
console.log(maior<=maior1);

// ==igual(valor)
console.log(maior==maior1);

// ===igual estrita checa o tipo
console.log(maior!= maior1);


// !=diferente
console.log(maior!=maior1);

// !== diferente estrito checa se sao do mesmo tipo
console.log(maior!==maior1);



