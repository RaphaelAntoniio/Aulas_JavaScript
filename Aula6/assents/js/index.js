//operadores aritimeticos
/*    +
*adição e concatenação exemplo*/
console.log(1+2); //adição
console.log('eu '+'te amo');//concatenação
/*    -
*subtração exemplo*/
console.log(1-2); //subtração
/*    /
* Divição exemplo*/
console.log(10/2); //divição
/*    *
* Multiplicação exemplo*/
console.log(10*2); //multiplicação
/*    **
*potenciação exemplo*/
console.log(4**2); //potenciaçao
/*    %
*resto da divição exemplo*/
console.log(3%2); //resto da divição
/* PRECENDENCIA
* 1- ()
* 2- **
* 3- * / %
* 4- + -   */
/*  ++ encremento -- incremento */
let contador = 0
console.log (++contador);
console.log(--contador);
/* += somar a variavel mais ela mesma pode ser feito com qualquer operador aritimetico*/






